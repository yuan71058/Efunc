package utils

import (
	"flag"
	"os"
)

// M命令行_解析 解析命令行参数。
// 调用 flag.Parse() 解析 os.Args[1:] 中的命令行参数。
// 应在定义所有命令行参数后调用此函数。
func M命令行_解析() {
	flag.Parse()
}

// M命令行_取字符串参数 定义并获取一个字符串类型的命令行参数。
// 支持短参数名和长参数名两种形式，如 -n 和 -name。
//
// 参数:
//   - 短名称: 短参数名，如 "n"
//   - 长名称: 长参数名，如 "name"
//   - 默认值: 参数未提供时的默认值
//   - 说明: 参数的用法说明
//
// 返回:
//   - *string: 指向参数值的指针，解析后通过 *指针 获取实际值
func M命令行_取字符串参数(短名称 string, 长名称 string, 默认值 string, 说明 string) *string {
	return flag.String(短名称, 默认值, 说明)
}

// M命令行_取整数参数 定义并获取一个整数类型的命令行参数。
//
// 参数:
//   - 短名称: 短参数名，如 "p"
//   - 长名称: 长参数名，如 "port"
//   - 默认值: 参数未提供时的默认值
//   - 说明: 参数的用法说明
//
// 返回:
//   - *int: 指向参数值的指针
func M命令行_取整数参数(短名称 string, 长名称 string, 默认值 int, 说明 string) *int {
	return flag.Int(短名称, 默认值, 说明)
}

// M命令行_取布尔参数 定义并获取一个布尔类型的命令行参数。
//
// 参数:
//   - 短名称: 短参数名，如 "v"
//   - 长名称: 长参数名，如 "verbose"
//   - 默认值: 参数未提供时的默认值
//   - 说明: 参数的用法说明
//
// 返回:
//   - *bool: 指向参数值的指针
func M命令行_取布尔参数(短名称 string, 长名称 string, 默认值 bool, 说明 string) *bool {
	return flag.Bool(短名称, 默认值, 说明)
}

// M命令行_取小数参数 定义并获取一个浮点数类型的命令行参数。
//
// 参数:
//   - 短名称: 短参数名，如 "r"
//   - 长名称: 长参数名，如 "rate"
//   - 默认值: 参数未提供时的默认值
//   - 说明: 参数的用法说明
//
// 返回:
//   - *float64: 指向参数值的指针
func M命令行_取小数参数(短名称 string, 长名称 string, 默认值 float64, 说明 string) *float64 {
	return flag.Float64(短名称, 默认值, 说明)
}

// M命令行_取剩余参数 获取命令行中未被 flag 解析的位置参数。
// 必须在 M命令行_解析 之后调用。
//
// 返回:
//   - []string: 未被解析的位置参数列表
func M命令行_取剩余参数() []string {
	return flag.Args()
}

// M命令行_取程序名 获取当前运行的程序名称（os.Args[0]）。
//
// 返回:
//   - string: 程序名称或路径
func M命令行_取程序名() string {
	return os.Args[0]
}

// M命令行_置用法 设置命令行参数的用法说明。
// 当用户输入 -h 或 --help 时显示此说明。
//
// 参数:
//   - 用法函数: 自定义的用法说明函数
func M命令行_置用法(用法函数 func()) {
	flag.Usage = 用法函数
}

// M命令行_显示默认用法 显示默认的命令行参数用法说明。
func M命令行_显示默认用法() {
	flag.Usage()
}
