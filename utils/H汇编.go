package utils

import (
	"math/rand"
	"time"
)

// H汇编_取随机数 生成指定范围内的随机整数，包含边界值 [起始数, 结束数]。
// 每次调用都会重置随机种子为当前纳秒时间戳。
// 注意：高频调用时可能因种子相同导致结果重复，建议改用 Q取随机数。
//
// 参数:
//   - 起始数: 最小值（包含）
//   - 结束数: 最大值（包含）
//
// 返回:
//   - int: [起始数, 结束数] 范围内的随机整数
func H汇编_取随机数(起始数, 结束数 int) int {
	rand.NewSource(time.Now().UnixNano())
	return rand.Intn(结束数-起始数+1) + 起始数
}
